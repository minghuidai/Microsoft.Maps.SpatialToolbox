
namespace Microsoft.Maps.SpatialToolbox
{
    /// <summary>
    /// An enumerator of different types of distance units.
    /// </summary>
    public enum DistanceUnits
    {
        /// <summary>
        /// A distance in meters
        /// </summary>
        Meters,

        /// <summary>
        /// A distance in kilometers
        /// </summary>
        KM,

        /// <summary>
        /// A distance in miles
        /// </summary>
        Miles,

        /// <summary>
        /// A distance in Feet
        /// </summary>
        Feet,

        /// <summary>
        /// A distance in Yards
        /// </summary>
        Yards
    }
}
